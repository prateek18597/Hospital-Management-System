    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //First Check whether user can issue book or not based on it's type
        try
        {
            Connection myConn=null;
            Statement stat=null;
            ResultSet rs=null;
            String BookId=(String)jComboBox1.getSelectedItem();
            myConn=DriverManager.getConnection("jdbc:mysql://localhost:3306/LMS_0049",Info.user,Info.pass);
            stat=myConn.createStatement();
            rs=stat.executeQuery("Select count(*) from Book where UserId='"+Info.currentUserId+"'");
            int count=0;
            if(rs.next())
                count=Integer.parseInt(rs.getString(1));
            rs=stat.executeQuery("Select User_Type from User where Id='"+Info.currentUserId+"'");
            String type="Student";
            if(rs.next())
                type=rs.getString(1);
            if(type.equals("Student") && count>3)
            {
                JOptionPane.showMessageDialog(rootPane, "Limit Exhausted");
            }
            else if(type.equals("Faculty") && count>6)
            {
                JOptionPane.showMessageDialog(rootPane, "Limit Exhausted");
            }
            else if(type.equals("Staff") && count>4)
            {
                JOptionPane.showMessageDialog(rootPane, "Limit Exhausted");
            }
            else if(type.equals("Guest") && count>2)
            {
                JOptionPane.showMessageDialog(rootPane, "Limit Exhausted");
            }
            else
            {
                stat.executeUpdate("Update Book set Issue_date=curdate(),UserId='"+Info.currentUserId+"' where Id='"+BookId+"'");
                JOptionPane.showMessageDialog(rootPane, "Book Issued Successfully.");
                first();
            }  
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

